// FunFactSeeder seeds the database with initial fun facts
type FunFactSeeder struct {
	repo   repositories.FunFactRepository
	logger *slog.Logger
}

// NewFunFactSeeder creates a new FunFactSeeder
func NewFunFactSeeder(repo repositories.FunFactRepository, logger *slog.Logger) *FunFactSeeder {
	return &FunFactSeeder{repo: repo, logger: logger}
}

// SeedIfEmpty seeds fun facts only if the table is empty
func (s *FunFactSeeder) SeedIfEmpty(ctx context.Context) error {
	count, err := s.repo.Count(ctx)
	if err != nil {
		return fmt.Errorf("failed to count facts: %w", err)
	}

	if count > 0 {
		s.logger.Info("Fun facts already seeded", "count", count)
		return nil
	}

	facts := getDefaultFacts()
	if err := s.repo.BulkCreate(ctx, facts); err != nil {
		return fmt.Errorf("failed to seed fun facts: %w", err)
	}

	s.logger.Info("Fun facts seeded successfully", "count", len(facts))
	return nil
}

